package Operators;

public class RelationalOperators
{
	public static boolean equalsTo(int a, int b)
	{
		return a == b;
	}
	
	public static boolean notEqualsTo(int a, int b)
	{
		return a != b;
	}
	
	public static boolean lessThan(int a, int b)
	{
		return a < b;
	}
	
	public static boolean lessThenEqualsTo(int a, int b)
	{
		return a <= b;
	}
	
	public static boolean greaterThen(int a, int b)
	{
		return a > b;
	}
	
	public static boolean greaterThenEqualsTo(int a, int b)
	{
		return a >= b;
	}
}
